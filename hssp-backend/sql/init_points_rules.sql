-- 初始化积分兑换规则数据
-- 这些规则定义了步数如何转换为积分

INSERT INTO `points_rule` (`rule_name`, `steps_required`, `points_awarded`, `is_active`) 
VALUES 
('基础兑换规则', 1000, 1, 1),
('进阶兑换规则', 5000, 6, 0),
('高级兑换规则', 10000, 15, 0);

-- 说明：
-- 1. 基础兑换规则（已启用）：每1000步兑换1积分
-- 2. 进阶兑换规则（未启用）：每5000步兑换6积分（相当于每1000步1.2积分，更优惠）
-- 3. 高级兑换规则（未启用）：每10000步兑换15积分（相当于每1000步1.5积分，最优惠）
-- 4. is_active字段: 1表示启用，0表示禁用

-- 可以通过管理后台启用不同的规则，系统会按照步数要求从低到高依次应用所有启用的规则
-- 例如：如果同时启用了基础规则和进阶规则，用户有6000步时：
--   - 先用5000步兑换6积分（进阶规则）
--   - 剩余1000步兑换1积分（基础规则）
--   - 总共获得7积分
